namespace CalcLib
{
    internal class Calculation
    {
        internal int Square(int n1,int n2)
        {
            int result1 = n1 * n2;
            return result1;
        }
        internal int Cube(int n1,int n2,int n3)
        {
            int result2 = n1 * n2 * n3;
            return result2;
        }
    }
}